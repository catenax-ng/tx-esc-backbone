package keeper_test

import (
	"testing"

	sdk "github.com/cosmos/cosmos-sdk/types"
	"github.com/stretchr/testify/require"
	"google.golang.org/grpc/codes"
	"google.golang.org/grpc/status"

	keepertest "github.com/catenax/esc-backbone/testutil/keeper"
	"github.com/catenax/esc-backbone/testutil/nullify"
	"github.com/catenax/esc-backbone/x/ubcmm/types"
)

func TestCurveQuery(t *testing.T) {
	keeper, ctx := keepertest.UbcmmKeeper(t)
	wctx := sdk.WrapSDKContext(ctx)
	item := createTestCurve(keeper, ctx)
	tests := []struct {
		desc     string
		request  *types.QueryGetCurveRequest
		response *types.QueryGetCurveResponse
		err      error
	}{
		{
			desc:     "First",
			request:  &types.QueryGetCurveRequest{},
			response: &types.QueryGetCurveResponse{Curve: item},
		},
		{
			desc: "InvalidRequest",
			err:  status.Error(codes.InvalidArgument, "invalid request"),
		},
	}
	for _, tc := range tests {
		t.Run(tc.desc, func(t *testing.T) {
			response, err := keeper.Curve(wctx, tc.request)
			if tc.err != nil {
				require.ErrorIs(t, err, tc.err)
			} else {
				require.NoError(t, err)
				require.Equal(t,
					nullify.Fill(tc.response),
					nullify.Fill(response),
				)
			}
		})
	}
}
